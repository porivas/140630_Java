package org.han.web;

public class CalcService {
	
	public double add(double v1, double v2){
		return v1+v2;
	}
	public double minus(double v1, double v2){
		return v1-v2;
	}
	public double mul(double v1, double v2){
		return v1*v2;
	}
	public double div(double v1, double v2){
		return v1/v2;
	}

	

}
