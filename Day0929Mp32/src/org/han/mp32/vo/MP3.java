package org.han.mp32.vo;

public class MP3 {
	
	private final String fileName;
	private final String dsec;
	
	public MP3(String fileName, String dsec) {
		super();
		this.fileName = fileName;
		this.dsec = dsec;
	}

	public String getFileName() {
		return fileName;
	}

	public String getDsec() {
		return dsec;
	}
	
	
}
